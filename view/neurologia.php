        <p>Un neurólogo es un médico que tiene una formación adicional en las enfermedades que afectan al sistema nervioso central y al sistema neuromuscular. Los neurólogos tratan trastornos del sistema nervioso (cerebro, médula espinal y nervios periféricos), trastornos musculares, y dolor, especialmente dolor de cabeza. Los neurólogos tienen competencia sobre diversos trastornos médicos, incluyendo enfermedad cerebrovascular, enfermedad de Parkinson, dolores de cabeza, epilepsia, esclerosis múltiple, neuropatías periféricas, infecciones del sistema nervioso, tumores, y complicaciones de los traumatismos craneales y espinales.</p>
